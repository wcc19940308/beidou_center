package com.ctbt.beidou.base.bo;

import com.ctbt.beidou.base.utils.ValidityUtil.RuleType;

/**
 * 保存验证规则
 * @author zhangheng
 *
 */
public class ValidityRuleBo {

	private RuleType ruleType;

	private String className;

	private String fieldName;

	private String fieldText;

	private int min;

	private int max;

	private String regx;

	public String getRegx() {
		return regx;
	}

	public void setRegx(String regx) {
		this.regx = regx;
	}

	public ValidityRuleBo(RuleType ruleType, String className, String fieldName, String fieldText) {
		this.ruleType = ruleType;
		this.className = className;
		this.fieldName = fieldName;
		this.fieldText = fieldText;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public RuleType getRuleType() {
		return ruleType;
	}

	public void setRuleType(RuleType ruleType) {
		this.ruleType = ruleType;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public String getFieldText() {
		return fieldText;
	}

	public void setFieldText(String fieldText) {
		this.fieldText = fieldText;
	}
}
