package com.ctbt.beidou.data;

import java.util.List;

import com.ctbt.beidou.base.model.BdRecvPackage;

public class RecvBo {

	private String flag;
	private String msg;
	private List<BdRecvPackage> data;

	public RecvBo(String flag, String msg, List<BdRecvPackage> data) {
		this.flag = flag;
		this.msg = msg;
		this.data = data;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public List<BdRecvPackage> getData() {
		return data;
	}

	public void setData(List<BdRecvPackage> data) {
		this.data = data;
	}


}
